primeiro_numero = float(input("Digite o primeiro número: "))
segundo_numero = float(input("Digite o segundo número: "))

#MAIOR QUE
print(primeiro_numero > segundo_numero)

#MENOR QUE
print(primeiro_numero < segundo_numero)

#MAIOR OU IGUAL A
print(primeiro_numero >= segundo_numero)

#MENOR OU IGUAL A
print(primeiro_numero <= segundo_numero)

#IGUAL A
print(primeiro_numero == segundo_numero)

#DIFERENTE DE
print(primeiro_numero != segundo_numero)
